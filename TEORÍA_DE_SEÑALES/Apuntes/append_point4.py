        '\n',
        '# Regresión logística con Z_k\n',
        'logreg = LogisticRegression(random_state=2026, max_iter=1000)\n',
        'logreg.fit(Z_train, y_train)\n',
        'y_pred_logreg = logreg.predict(Z_test)\n',
        '\n',
        '# MLP con Z_k\n',
        'mlp_z = MLPClassifier(hidden_layer_sizes=(16,), activation="relu", solver="adam", max_iter=500, random_state=2026)\n',
        'mlp_z.fit(Z_train, y_train)\n',
        'y_pred_mlp_z = mlp_z.predict(Z_test)\n',
        '\n',
        '# MLP con todo el bloque r_k\n',
        'scaler = StandardScaler()\n',
        'X_train_full_scaled = scaler.fit_transform(X_train_full)\n',
        'X_val_full_scaled = scaler.transform(X_val_full)\n',
        'X_test_full_scaled = scaler.transform(X_test_full)\n',
        'mlp_full = MLPClassifier(hidden_layer_sizes=(32, 16), activation="relu", solver="adam", max_iter=500, random_state=2026)\n',
        'mlp_full.fit(X_train_full_scaled, y_train)\n',
        'y_pred_mlp_full = mlp_full.predict(X_test_full_scaled)\n',
        '\n',
        'results = {}\n',
        'for name, y_pred in [\n',
        '    ("Scalar MAP", y_pred_scalar),\n',
        '    ("Matched-filter MAP", y_pred_matched),\n',
        '    ("Logistic regression (Z)", y_pred_logreg),\n',
        '    ("MLP (Z)", y_pred_mlp_z),\n',
        '    ("MLP (full r)", y_pred_mlp_full),\n',
        ']:\n',
        '    PFA, PM, PD, Pe = compute_metrics(y_test, y_pred)\n',
        '    results[name] = {\n',
        '        "PFA": PFA,\n',
        '        "PM": PM,\n',
        '        "PD": PD,\n',
        '        "Pe": Pe,\n',
        '        "cm": confusion_matrix(y_test, y_pred),\n',
        '    }\n',
        '\n',
        'print("Resultados en el conjunto de prueba:")\n',
        'print("{:<28}{:>8}{:>8}{:>8}{:>8}".format("Detector", "P_e", "P_FA", "P_M", "P_D"))\n',
        'for name, info in results.items():\n',
        '    print("{:28}{:8.5f}{:8.5f}{:8.5f}{:8.5f}".format(name, info["Pe"], info["PFA"], info["PM"], info["PD"]))\n',
        '\n',
        'print("\nMatrices de confusión:")\n',
        'for name, info in results.items():\n',
        '    print(name)\n',
        '    print(info["cm"])\n',
