        'from sklearn.model_selection import train_test_split\n',
        'from sklearn.linear_model import LogisticRegression\n',
        'from sklearn.neural_network import MLPClassifier\n',
        'from sklearn.preprocessing import StandardScaler\n',
        'from sklearn.metrics import confusion_matrix\n',
        '\n',
        '# Parámetros del experimento\n',
        'L = 8\n',
        'N_total = 30000\n',
        'rng = np.random.default_rng(2026)\n',
        '\n',
        '# Generamos el dataset simulado\n',
        'labels = rng.choice([0, 1], size=N_total, p=[0.5, 0.5])\n',
        'symbols = np.where(labels == 0, a0, a1)\n',
        'noise = rng.normal(0, sigma_eta, size=(N_total, L))\n',
        'r_blocks = symbols[:, None] + noise\n',
        'Z_blocks = r_blocks.mean(axis=1)\n',
        '\n',
        '# Particiones: entrenamiento 60%, validación 20%, prueba 20%\n',
        'X_train_full, X_temp_full, y_train, y_temp = train_test_split(\n',
        '    r_blocks, labels, test_size=0.4, stratify=labels, random_state=2026\n',
        ')\n',
        'X_val_full, X_test_full, y_val, y_test = train_test_split(\n',
        '    X_temp_full, y_temp, test_size=0.5, stratify=y_temp, random_state=2026\n',
        ')\n',
        '\n',
        'Z_train = X_train_full.mean(axis=1).reshape(-1, 1)\n',
        'Z_val = X_val_full.mean(axis=1).reshape(-1, 1)\n',
        'Z_test = X_test_full.mean(axis=1).reshape(-1, 1)\n',
        'X_train_scalar = X_train_full[:, 0].reshape(-1, 1)\n',
        'X_val_scalar = X_val_full[:, 0].reshape(-1, 1)\n',
        'X_test_scalar = X_test_full[:, 0].reshape(-1, 1)\n',
        '\n',
        'def compute_metrics(y_true, y_pred):\n',
        '    tn, fp, fn, tp = confusion_matrix(y_true, y_pred).ravel()\n',
