        '    PD = tp / (tp + fn) if (tp + fn) > 0 else 0.0\n',
        '    Pe = (fp + fn) / (fp + fn + tp + tn)\n',
        '    return PFA, PM, PD, Pe\n',
        '\n',
        '# Detectores analíticos\n',
        'threshold = (a0 + a1) / 2\n',
        'y_pred_scalar = np.where(X_test_scalar[:, 0] >= threshold, 1, 0)\n',
